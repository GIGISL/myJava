package fc.java.part2;

public class FourArithmethic {
    public static void main(String[] args) {
        int a, b;
        a=12;
        b=2;
        System.out.println("a+b="+(a+b));
        System.out.println("a-b="+(a-b));
        System.out.println("a*b="+(a*b));
        System.out.println("a/b="+(a/b));
    }
}
